void TN_Lcd_show_ascii(UINT8 col,UINT8 page,UINT8 chs)
{
    UINT8 i;
    UINT32 x;
    UINT8 ComTable[]={3,2,1,0,7,6,5,4};
    x = (chs-0x20)*0x10;

    sAPI_LcdWriteCmd(ComTable[page&0x07]|0xB0); //Set Page Address
    sAPI_LcdWriteCmd( ((col+1)>>4) | 0x10); //Set Column Address High Byte
    sAPI_LcdWriteCmd( (col+1)&0x0F ); //Low Byte Colum from S128 -> S1 auto add
    for(i=0;i<8;i++)
    {
        sAPI_LcdWriteData(ASC16[x],1);
        x++;
    }
    page++; //下半字符 page+1
    sAPI_LcdWriteCmd(ComTable[page&0x07]|0xB0); //Set Page Address
    sAPI_LcdWriteCmd( ((col+1)>>4) | 0x10); //Set Column Address High Byte
    sAPI_LcdWriteCmd( (col+1)&0x0F ); //Low Byte Colum from S128 -> S1 auto add
    for(i=0;i<8;i++)
    {
        sAPI_LcdWriteData(ASC16[x],1);
        x++;
    }
    page--; //写完一个字符 page 还原
}

